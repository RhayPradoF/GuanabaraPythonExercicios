#desafio8 >> Escreva um programa que leia um valor em metros e o exiba convertido em centimetros e milímetros

m = float(input("Informe um tamanho em metros:" ))

cm =m*100
mm = m*1000

print("O tamanho informado em centimetros é: ", cm, " cm")
print("O tamanho informado em milímetros é: ", mm," mm")